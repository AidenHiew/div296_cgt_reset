/*
 * build-spfx-markup.mjs — derive the SPFx web part's markup + CSS from the
 * verified web/ source, so the SharePoint build can't drift from the site.
 *
 * Pulls web/styles.css and the <section id="calculator"> block out of
 * web/index.html and emits src/webparts/div296Calculator/markup.ts.
 *
 *   node docs/hosting/spfx/build-spfx-markup.mjs   (run from repo root)
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repo = join(here, "..", "..", "..");
const read = (p) => readFileSync(join(repo, p), "utf8");

const css = read("web/styles.css");
const html = read("web/index.html");

// Grab the calculator section (results cards, inputs, breakdown, fund figures).
const m = html.match(/<section id="calculator"[\s\S]*?<\/section>/);
if (!m) throw new Error("could not find the calculator <section> in index.html");
const section = m[0];

const esc = (s) => s.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");

const out = `// AUTO-GENERATED by docs/hosting/spfx/build-spfx-markup.mjs — do not edit.
// Source of truth: web/styles.css + web/index.html. Re-run the generator to refresh.
/* eslint-disable */
export const CSS: string = \`${esc(css)}\`;

export const HTML: string = \`${esc(section)}\`;
`;

const dest = join(here, "src", "webparts", "div296Calculator");
mkdirSync(dest, { recursive: true });
writeFileSync(join(dest, "markup.ts"), out);
console.log(`wrote ${join(dest, "markup.ts")} (${out.length} bytes)`);
